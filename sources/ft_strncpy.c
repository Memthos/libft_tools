#include "../includes/libft.h"

char	*ft_strncpy(char *dest, const char *src, size_t size)
{
	size_t	i;

	if (size < 1)
		return (dest);
	i = 0;
	while (i < size && src[i])
	{
		dest[i] = src[i];
		i++;
	}
	while (i < size)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}
